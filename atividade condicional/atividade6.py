a = float(input("Digite um valor para A: "))
b = float(input("Digite um valor para B: "))
c = float(input("Digite um valor para C: "))
d = float(input("Digite um valor para D: "))

if a % 2 == 0:
    print(f"{a} é divisível")
else: 
    print(f"{a} não é divisível")

if b % 2 == 0:
    print(f"{b} é divisível")
else: 
    print(f"{b} não é divisível")

if c % 2 == 0:
    print(f"{c} é divisível")
else: 
    print(f"{c} não é divisível")

if d % 2 == 0:
    print(f"{d} é divisível")
else: 
    print(f"{d} não é divisível")